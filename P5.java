// Wajp to find avg of three numbers

class P5


{
static public void main(String ... a)
{
   double N1= Double.parseDouble(a[0]);
   double N2= Double.parseDouble(a[1]);
   double N3= Double.parseDouble(a[2]);
   double avg=(N1 + N2 + N3)/3;
System.out.println("avg=" + String.format("%.2f",avg));
}

}


